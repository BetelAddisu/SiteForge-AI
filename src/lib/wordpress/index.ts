const ENCRYPTION_KEY = process.env.ENCRYPTION_KEY || 'siteforge-default-key';

function encryptPassword(password: string): string {
  return Buffer.from(password).toString('base64');
}

function decryptPassword(encrypted: string): string {
  return Buffer.from(encrypted, 'base64').toString('utf8');
}

/**
 * Save WordPress connection for a user
 */
export async function saveWordPressConnection(
  userId: string,
  siteUrl: string,
  username: string,
  appPassword: string
): Promise<void> {
  const prisma = new PrismaClient();
  
  await prisma.wordPressConnection.upsert({
    where: { userId },
    create: {
      userId,
      siteUrl,
      username,
      appPassword: encryptPassword(appPassword),
    },
    update: {
      siteUrl,
      username,
      appPassword: encryptPassword(appPassword),
    },
  });
}

/**
 * Get WordPress connection for a user
 */
export async function getWordPressConnection(userId: string): Promise<WordPressConfig | null> {
  const prisma = new PrismaClient();
  
  const connection = await prisma.wordPressConnection.findUnique({
    where: { userId },
  });
  
  if (!connection) return null;
  
  return {
    siteUrl: connection.siteUrl,
    username: connection.username,
    appPassword: decryptPassword(connection.appPassword),
  };
}

/**
 * Remove WordPress connection
 */
export async function removeWordPressConnection(userId: string): Promise<void> {
  const prisma = new PrismaClient();
  
  await prisma.wordPressConnection.delete({
    where: { userId },
  });
}

/**
 * Publish website to WordPress
 */
export async function publishToWordPress(
  userId: string,
  projectId: string,
  pages: Array<{
    title: string;
    slug: string;
    elementorData: unknown[];
    status?: 'draft' | 'publish';
  }>
): Promise<{
  success: boolean;
  publishedPages?: Array<{ title: string; url: string }>;
  errors?: string[];
}> {
  const connection = await getWordPressConnection(userId);
  
  if (!connection) {
    return { success: false, errors: ['WordPress connection not configured'] };
  }
  
  const client = new WordPressClient(connection);
  const publishedPages: Array<{ title: string; url: string }> = [];
  const errors: string[] = [];
  
  for (const page of pages) {
    const result = await client.publishElementorPage(page.title, page.elementorData, {
      slug: page.slug,
      status: page.status,
    });
    
    if (result.success && result.pageUrl) {
      publishedPages.push({ title: page.title, url: result.pageUrl });
      
      const prisma = new PrismaClient();
      await prisma.deployment.create({
        data: {
          projectId,
          wordpressUrl: result.pageUrl,
          pageId: String(result.pageId),
          status: 'COMPLETE',
        },
      });
    } else {
      errors.push(`${page.title}: ${result.error}`);
    }
  }
  
  return {
    success: errors.length === 0,
    publishedPages,
    errors: errors.length > 0 ? errors : undefined,
  };
}
